class UserRequest{
  final String name;
  final String email;
  final String password;
  final int age;
  final String phone;
  final String gender;
  final String city;

  UserRequest({
    required this.name,
    required this.email,
    required this.password,
    required this.age,
    required this.phone,
    required this.gender,
    required this.city,
  });
}